package switch_case;



public class switch_7
{
    void main(String cat)
   {
       switch(cat)
       {
           case "how are you?":
           System.out.println("i am fine");
           break;
           case "who are you":
           System.out.println("computer");
           break;
           default:
           System.out.println("u are mad");
           
        }
    }
}